int		ft_atoi(const char *str)
{
	int		nb;
	int		neg;

	nb = 0;
	neg = 0;
	while (*str == '\n' || *str == '\t' || *str == '\v' || *str == ' '
			|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
		neg = 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	if (neg == 1)
		nb = -nb;
	return (nb);
}
