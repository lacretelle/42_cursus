#include "fdf.h"

void	fd_delete(void ***p)
{
	int		i;
	void	**tab;

	i = 0;
	if ((p))
	{
		tab = *p;
		while (tab[i] != NULL)
		{
			ft_memdel(&tab[i]);
			i++;
		}
		free(tab);
		*p = NULL;
	}
}
